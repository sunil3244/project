
//Q 2  Wap to swap two numbers

package Assignment_01;

public class Que_02_swap {
	public static void main(String[] args)
	{
		int x=100;
		int y=200;
		System.out.println("Befour swaping   X="+x +"  Y="+y);
		int temp;
		temp=x;
		x=y;
		y=temp;
		System.out.println("After swaping   X="+x +"  Y="+y);
		
	}

}
